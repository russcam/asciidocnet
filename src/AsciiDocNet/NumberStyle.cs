namespace AsciiDocNet
{
    /// <summary>
    /// The number style for an <see cref="OrderedListItem"/>
    /// </summary>
    public enum NumberStyle
    {
        /// <summary>
        /// Arabic
        /// </summary>
        Arabic,
        /// <summary>
        /// Decimal
        /// </summary>
        Decimal,
        /// <summary>
        /// Lowercase Alphabetical
        /// </summary>
        LowerAlpha,
        /// <summary>
        /// Uppercase Alphabetical
        /// </summary>
        UpperAlpha,
        /// <summary>
        /// Lowercase Roman Numeral
        /// </summary>
        LowerRoman,
        /// <summary>
        /// Uppercase Roman Numeral
        /// </summary>
        UpperRoman,
        /// <summary>
        /// Lowercase Greek
        /// </summary>
        LowerGreek
    }
}